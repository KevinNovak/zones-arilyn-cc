<h2>2024a <span class="small text-muted">(2024-02-01)</span></h2>
<ul>
	<li>Kazakhstan unifies on UTC+5 beginning 2024-03-01.</li>
	<li>Palestine springs forward a week later after Ramadan.</li>
</ul>

<h2>2023d <span class="small text-muted">(2023-12-21)</span></h2>
<ul>
	<li>Ittoqqortoormiit, Greenland changes time zones on 2024-03-31.</li>
	<li>Vostok, Antarctica changed time zones on 2023-12-18.</li>
	<li>Casey, Antarctica changed time zones five times since 2020.</li>
</ul>

<h2>2023c <span class="small text-muted">(2023-03-28)</span></h2>
<ul>
	<li>Model Lebanon's DST chaos by reverting data to tzdb 2023a.</li>
</ul>

<h2>2023b <span class="small text-muted">(2023-03-23)</span></h2>
<ul>
	<li>This year Lebanon springs forward April 20/21 not March 25/26.</li>
</ul>

<h2>2023a <span class="small text-muted">(2023-03-22)</span></h2>
<ul>
	<li>Egypt now uses DST again, from April through October.</li>
	<li>This year Morocco springs forward April 23, not April 30.</li>
	<li>Palestine delays the start of DST this year.</li>
	<li>Much of Greenland still uses DST from 2024 on.</li>
	<li>America/Yellowknife now links to America/Edmonton.</li>
</ul>
